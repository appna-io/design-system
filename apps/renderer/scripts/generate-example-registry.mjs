#!/usr/bin/env node
/**
 * Generates `apps/renderer/src/generated/exampleRegistry.ts` — a static map of
 * `"<Component>/<Example>" → () => import("...")` factories.
 *
 * Why this exists:
 *   The previous renderer used a template-literal dynamic `import()` of the form
 *   `import(`@apx-ui/components/src/${dirName}/examples/${exampleId}.tsx`)`.
 *   Webpack interprets that as a *context* request and statically discovers every
 *   matching file across `packages/components/src/`, then bundles them all together.
 *   Every component-page navigation in `next dev` had to compile that giant context
 *   before rendering. Switching to a generated registry of *static* `import()` calls
 *   gives webpack one chunk per example — only the example you actually visit is
 *   compiled.
 *
 * Lifecycle:
 *   - Runs once on `pnpm dev` (via the renderer's `predev` script).
 *   - Runs once on `pnpm build` (via the renderer's `prebuild` script).
 *   - Add or remove an example → restart the dev server (the script regenerates).
 *   - The output file is gitignored.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rendererRoot = path.resolve(__dirname, '..');
const componentsSrc = path.resolve(rendererRoot, '../../packages/components/src');
const outFile = path.join(rendererRoot, 'src/generated/exampleRegistry.ts');

function listDirs(root) {
  return fs
    .readdirSync(root, { withFileTypes: true })
    .filter((e) => e.isDirectory() && !e.name.startsWith('_') && !e.name.startsWith('.'))
    .map((e) => e.name)
    .sort();
}

function listExamples(componentDir) {
  const examplesDir = path.join(componentDir, 'examples');
  if (!fs.existsSync(examplesDir)) return [];
  return fs
    .readdirSync(examplesDir, { withFileTypes: true })
    .filter((e) => e.isFile() && /\.(tsx|jsx)$/.test(e.name))
    .map((e) => path.basename(e.name, path.extname(e.name)))
    .sort();
}

function main() {
  const components = listDirs(componentsSrc);
  const entries = [];
  for (const dir of components) {
    for (const example of listExamples(path.join(componentsSrc, dir))) {
      const key = `${dir}/${example}`;
      const importPath = `@apx-ui/components/src/${dir}/examples/${example}.tsx`;
      entries.push({ key, importPath });
    }
  }

  const banner = `// AUTO-GENERATED by scripts/generate-example-registry.mjs. Do not edit by hand.\n// Regenerated on \`pnpm dev\` / \`pnpm build\` via the renderer's predev/prebuild hooks.\n`;

  const body = entries
    .map(
      ({ key, importPath }) =>
        `  ${JSON.stringify(key)}: () => import(${JSON.stringify(importPath)}),`,
    )
    .join('\n');

  const out = `${banner}
import type { ComponentType } from 'react';

export type ExampleModule = { default: ComponentType };
export type ExampleLoader = () => Promise<ExampleModule | Record<string, ComponentType>>;

/**
 * One entry per \`<ComponentDir>/<ExampleId>\`. Each value is a static \`import()\` so webpack
 * can code-split per example — visiting one component page no longer compiles every example
 * across every component.
 */
export const exampleRegistry: Record<string, ExampleLoader> = {
${body}
};

export function getExampleLoader(dirName: string, exampleId: string): ExampleLoader | null {
  return exampleRegistry[\`\${dirName}/\${exampleId}\`] ?? null;
}
`;

  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, out, 'utf8');
  process.stdout.write(
    `[example-registry] wrote ${entries.length} entries → ${path.relative(rendererRoot, outFile)}\n`,
  );
}

main();
